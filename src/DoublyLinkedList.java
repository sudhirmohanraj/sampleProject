public class DoublyLinkedList {
    int i;
    DoublyLinkedList next;
    DoublyLinkedList prev;

    public DoublyLinkedList(int i, DoublyLinkedList next, DoublyLinkedList prev) {
        this.i = i;
        this.next = next;
        this.prev = prev;
    }
}
